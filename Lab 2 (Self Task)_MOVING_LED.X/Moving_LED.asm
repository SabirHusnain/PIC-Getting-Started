#include "P18F452.inc"
LIST P=18F452, F=INHX32, MM=OFF
    
CONFIG OSC=XT
CONFIG WDT=OFF
    
ORG 0x00
GOTO MAIN
ORG 0x200
    
MAIN:
    
    R2 EQU 0x2
    R3 EQU 0x3
 
    MOVLW 0x00
    MOVWF TRISB
    MOVLW B'10000000'
    MOVWF PORTB
    
    ENDLESS_LOOP
	RLNCF PORTB, F
	CALL Delay
	BRA ENDLESS_LOOP
	ORG 0x2000
    
    Delay ; 500ms Delay, F=10MHz
    
		MOVLW D'00'
		MOVWF R3
	AGAIN	MOVLW D'250'
		MOVWF R2
	HERE	NOP
		NOP
		DECF	R2,F
		BNZ HERE
		DECF	R3,F
		BNZ AGAIN
		RETURN
END